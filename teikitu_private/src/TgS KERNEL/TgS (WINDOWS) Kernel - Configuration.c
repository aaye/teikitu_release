/* ---- tgKN_OS_Set_Default_Class_Name ------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgKN_OS_Set_Default_Class_Name( TgOSCHAR_CPC szClassName )
{
    TgERROR( !tgKN_Query_Boot() );

    if (nullptr == szClassName)
    {
        TgOS_TEXT_FCN(Copy)( g_szKN_Default_Class_Name, KTgKN_MAX_TITLE, TgOS_TEXT("Teikitu_Class"), KTgMAX_RSIZE );
    }
    else
    {
        TgOS_TEXT_FCN(Copy)( g_szKN_Default_Class_Name, KTgKN_MAX_TITLE, szClassName, KTgMAX_RSIZE );
    };
}


/* ---- tgKN_OS_Set_Default_Window_Title ----------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgKN_OS_Set_Default_Window_Title( TgOSCHAR_CPC sExeName )
{
    TgERROR( !tgKN_Query_Boot() );

    if (nullptr == sExeName)
    {
        TgOS_TEXT_FCN(Copy)( g_szKN_Default_Window_Title, KTgKN_MAX_TITLE, TgOS_TEXT("Teikitu_Default"), KTgMAX_RSIZE );
    }
    else
    {
        TgOS_TEXT_FCN(Copy)( g_szKN_Default_Window_Title, KTgKN_MAX_TITLE, sExeName, KTgMAX_RSIZE );
    };
}


