/* ---- tgPH_Set_Pause_Module ---------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgPH_Set_Pause_Module( TgBOOL_C bPaused )
{
    if (KTgID__INVALID_VALUE != g_tiPH_CN_Module__Paused.m_uiKI)
    {
        tgCN_Var_Set_Bool( g_tiPH_CN_Module__Paused, bPaused );
    };
}


/* ---- tgPH_Is_Module_Paused ---------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgBOOL tgPH_Is_Module_Paused( TgVOID )
{
    return (g_bPH_Module__Paused);
}


/* ---- tgPH_Set_Iteration_Limit ------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgPH_Set_Iteration_Limit( TgUINT_E64_C uiLoop_Max )
{
    if (KTgID__INVALID_VALUE != g_tiPH_CN_Module__Loop_Max.m_uiKI)
    {
        tgCN_Var_Set_U64( g_tiPH_CN_Module__Loop_Max, uiLoop_Max );
    };
}


/* ---- tgPH_Query_Iteration_Limit ----------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgUINT_E64 tgPH_Query_Iteration_Limit( TgVOID )
{
    return (g_uiPH_Module__Loop_Max);
}




