/* ---- tgPM_MT_MX_Init ---------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgRESULT tgPM_MT_MX_Init( STg1_MT_MX_PCU psMX, TgOSCHAR_CPCU szName )
{
    return ((nullptr == (psMX->m_hMutex = g_pfnCreateMutexEx( nullptr, szName, 0, 0x1F0001 ))) ? KTgE_FAIL : KTgS_OK);
}


/* ---- tgPM_MT_MX_Free ---------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgPM_MT_MX_Free( STg1_MT_MX_PCU psMX )
{
    if (0 != psMX->m_hMutex)
    {
        g_pfnCloseHandle( psMX->m_hMutex );
        psMX->m_hMutex = 0;
    };
}


/* ---- tgPM_MT_MX_Wait_Block ---------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgPM_MT_MX_Wait_Block( STg1_MT_MX_PCU psMX )
{
    while (0UL != g_pfnWaitForSingleObjectEx( psMX->m_hMutex, 0xFFFFFFFF, true ));
}


/* ---- tgPM_MT_MX_Wait ---------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgRESULT tgPM_MT_MX_Wait( STg1_MT_MX_PCU psMX )
{
    return(0UL == g_pfnWaitForSingleObjectEx( psMX->m_hMutex, 0, true ) ? KTgS_OK : KTgE_FAIL);
}


/* ---- tgPM_MT_MX_Release ------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgPM_MT_MX_Release( STg1_MT_MX_PCU psMX )
{
    g_pfnReleaseMutex( psMX->m_hMutex );
}
