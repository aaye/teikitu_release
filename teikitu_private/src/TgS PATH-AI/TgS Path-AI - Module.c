/* ---- tgAI_Query_Init ---------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgBOOL tgAI_Query_Init( TgVOID )
{
    return (ETgMODULE_STATE__INITIALIZED <= s_enModule_State && s_enModule_State <= ETgMODULE_STATE__STOPPED);
}


/* ---- tgAI_Query_Boot ---------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgBOOL tgAI_Query_Boot( TgVOID )
{
    return (ETgMODULE_STATE__BOOTED == s_enModule_State);
}
