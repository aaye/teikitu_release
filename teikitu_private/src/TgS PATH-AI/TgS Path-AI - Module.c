/* ---- tgAI_Module_Init --------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgRESULT tgAI_Module_Init( TgVOID )
{
    /* Verify the state of the system */
    TgERROR( ETgMODULE_STATE__FREED == s_enModule_State );
    s_enModule_State = ETgMODULE_STATE__INITIALIZING;

    s_enModule_State = ETgMODULE_STATE__INITIALIZED;
    return (KTgS_OK);
}


/* ---- tgAI_Module_Boot --------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgRESULT tgAI_Module_Boot( TgVOID )
{
    /* Verify the state of the system */
    TgERROR( ETgMODULE_STATE__INITIALIZED == s_enModule_State );
    s_enModule_State = ETgMODULE_STATE__BOOTING;

    s_enModule_State = ETgMODULE_STATE__BOOTED;
    return (KTgS_OK);
}


/* ---- tgAI_Module_Stop --------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgAI_Module_Stop( TgVOID )
{
    if ((ETgMODULE_STATE__FREED == s_enModule_State) || (ETgMODULE_STATE__INITIALIZED == s_enModule_State))
    {
        return;
    };

    /* Verify the state of the system */
    TgERROR( ETgMODULE_STATE__BOOTED == s_enModule_State );
    s_enModule_State = ETgMODULE_STATE__STOPPING;

    s_enModule_State = ETgMODULE_STATE__STOPPED;
}


/* ---- tgAI_Module_Free --------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgAI_Module_Free( TgVOID )
{
    if (ETgMODULE_STATE__FREED == s_enModule_State)
    {
        return;
    };

    /* Verify the state of the system */
    TgERROR( ETgMODULE_STATE__STOPPED == s_enModule_State || ETgMODULE_STATE__INITIALIZED == s_enModule_State );
    s_enModule_State = ETgMODULE_STATE__FREEING;

    s_enModule_State = ETgMODULE_STATE__FREED;
}


